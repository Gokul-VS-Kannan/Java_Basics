package arithamatic_operators;

public class division {
    public static void main(String[] args){
        int num1 = 25;
        int num2 = 5;

        System.out.println(num1/num2);
        
        /* when dividing two iniger in java the result will also be an intiger */
        int num3 = 15;
        int num4 = 2;
        System.out.println(num3/num4); // output: 7//

        /* if we want decimal result use double values */
        double num5 = 22.0d;
        double num6 = 7.0d;
        System.out.println(num5/num6);

    }    
}
